OUTPUT_JSON = Path("pera_address_labels.json")

links_used = {}

def save_progress():
    data = {
        "address_labels": address_labels,
        "links_used": links_used,
    }

    OUTPUT_JSON.write_text(
        json.dumps(data, indent=4),
        encoding="utf-8"
    )


# Resume from previous run if JSON already exists
if OUTPUT_JSON.exists():
    saved = json.loads(OUTPUT_JSON.read_text(encoding="utf-8"))

    saved_labels = saved.get("address_labels", {})
    saved_links = saved.get("links_used", {})

    for address, label in saved_labels.items():
        if address in address_labels and label:
            address_labels[address] = label

    links_used.update(saved_links)


with sync_playwright() as p:
    browser = p.chromium.launch_persistent_context(
        user_data_dir="pera_browser_profile",
        headless=False
    )

    page = browser.new_page()

    first_unlabeled = None

    for address, label in address_labels.items():
        if not label:
            first_unlabeled = address
            break

    if first_unlabeled is None:
        print("Everything is already labeled.")
        browser.close()
        raise SystemExit

    first_url = f"https://explorer.perawallet.app/address/{first_unlabeled}/"
    page.goto(first_url, wait_until="domcontentloaded", timeout=60000)

    print("Waiting 10 seconds for Cloudflare verification...")
    time.sleep(10)

    html = page.content()

    if (
        "Performing security verification" in html
        or "Cloudflare" in html
        or "Attention Required" in html
        or "Sorry, you have been blocked" in html
    ):
        input("Finish verification in the opened browser, then press Enter here...")

    for index, address in enumerate(address_labels, start=1):
        current_label = address_labels[address]

        if current_label:
            print(f"[{index}/{len(address_labels)}] SKIP: {address} -> {current_label}")
            continue

        url = f"https://explorer.perawallet.app/address/{address}/"
        links_used[address] = url

        try:
            page.goto(url, wait_until="domcontentloaded", timeout=60000)

            time.sleep(3)

            page.wait_for_selector(
                "span.account-address__name",
                timeout=20000
            )

            label = page.locator(
                "span.account-address__name"
            ).first.inner_text().strip()

            address_labels[address] = label

            print(f"[{index}/{len(address_labels)}] FOUND: {address} -> {label}")

        except Exception as e:
            print(f"[{index}/{len(address_labels)}] FAILED: {address} -> {e}")

        # Save after every single address, success or fail
        save_progress()

        time.sleep(1)

    browser.close()


save_progress()

